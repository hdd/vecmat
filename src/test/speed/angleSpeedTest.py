from vecmat.angle import Angle

#	SUM
def sumRad():
	'''
	rad + rad
	'''
	a1=Angle(0.1)
	a2=Angle(0.5)
	a1+a2

def sumDeg():
	'''
	deg + deg
	'''
	a1=Angle(90).setAsDeg()
	a2=Angle(90).setAsDeg()
	a1+a2	
	
def sumDegRad():
	'''
	deg + Rad
	'''
	a1=Angle(90).setAsDeg()
	a2=Angle(.1)
	a1+a2	
	
def sumRadDeg():
	'''
	Rad + Deg
	'''
	a1=Angle(.1)
	a2=Angle(90).setAsDeg()
	a1+a2
		
def sumDegFloat():
	'''
	deg + float
	'''
	a1=Angle(90).setAsDeg()
	a2=10.0
	a1+a2
	
def sumRadFloat():
	'''
	rad + float
	'''
	a1=Angle(.1)
	a2=1.0
	a1+a2	

